"""direct_runner — pure-Python runner with zero external dependencies.

This runner implements the full gen / eval / report pipeline entirely in
Python. Attack payloads are generated by the `gen.generate_attacks` module;
each payload is a dict with the following keys:

    prompt         : str — the attack prompt text sent to the target model
    category       : str — OWASP category ID (e.g. "llm01_prompt_injection")
    category_name  : str — human-readable category name

Evaluation sends each prompt through Squid proxy to OpenClaw, records
whether arsguard blocked it, and obtains a judge verdict from qwen3-0.6b.
"""

import json
import os
import sys

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from lib.base import BaseRunner
from lib.judge import Judge
from lib.proxy import ProxyClient
from lib.reporter import calc_stats, gen_metadata, eval_metadata, save_json
from gen.generate_attacks import generate_all_attacks
from report.generate_report import render_markdown, save_reports


class DirectRunner(BaseRunner):
    """Full-pipeline runner using Python-only attack generation and evaluation.

    Implements the three-phase lifecycle mandated by BaseRunner:
      1. gen()    — generate attack prompts via `gen.generate_attacks`
      2. eval()   — send prompts through Squid → OpenClaw and record results
      3. report() — render a Markdown report with summary stats
    """

    def __init__(self, config: dict):
        """Initialise judge, proxy client, and verbosity flag.

        Args:
            config: Top-level eval configuration dict. Relevant keys:
                ollama.host         — Ollama API endpoint
                ollama.target_model — model name used by the judge
                squid.proxy         — Squid proxy URL
                squid.openclaw_url  — OpenClaw backend URL
                verbose             — print progress to stdout
        """
        super().__init__(config)
        self.judge = Judge(
            model=self.ollama_cfg.get("target_model", "qwen3-0.6b"),
            host=self.ollama_cfg.get("host", "http://localhost:11434"),
        )
        self.proxy = ProxyClient(
            proxy=self.squid_cfg.get("proxy", "http://localhost:3128"),
            openclaw_url=self.squid_cfg.get("openclaw_url", "http://openclaw:8080"),
        )
        self.verbose = config.get("verbose", False)

    def gen(self) -> list:
        """Phase 1: generate attack payloads and persist them to disk.

        Writes two files to the output directory:
          - generated_attacks.json          — full attack list
          - generated_attacks_promptfoo.json— promptfoo-compatible variant

        Also writes a gen_report.json with phase metadata.

        Returns:
            list[dict]: generated attack payloads.
        """
        attacks = generate_all_attacks(config=self.config)
        os.makedirs(self.output_dir, exist_ok=True)

        attack_path = os.path.join(self.output_dir, "generated_attacks.json")
        with open(attack_path, "w") as f:
            json.dump(attacks, f, ensure_ascii=False, indent=2)

        promptfoo_tests = [{"vars": a} for a in attacks]
        pf_path = os.path.join(self.output_dir, "generated_attacks_promptfoo.json")
        with open(pf_path, "w") as f:
            json.dump(promptfoo_tests, f, ensure_ascii=False, indent=2)

        meta = gen_metadata(attacks, self.config)
        save_json(meta, os.path.join(self.output_dir, "gen_report.json"))

        if self.verbose:
            print(f"[direct/gen] {len(attacks)} attacks generated \u2192 {attack_path}")
            print(f"[direct/gen] gen_report.json saved")
        return attacks

    def eval(self, test_cases: list) -> list:
        """Phase 2: send each attack prompt through the proxy and collect results.

        If *test_cases* is empty/falsy the method attempts to load them
        from the previously written generated_attacks.json.

        Each result dict captures:
          category / category_name — OWASP classification
          blocked_by_arsguard      — whether arsguard (Squid/OpenClaw) blocked it
          judge_verdict / judge_raw — LLM-based judgement on the response
          response                 — truncated proxy response

        Writes eval_results.json and eval_report.json.

        Args:
            test_cases: list of attack payloads (as returned by gen()).

        Returns:
            list[dict]: evaluation results, one per test case.
        """
        if not test_cases:
            path = os.path.join(self.output_dir, "generated_attacks.json")
            if os.path.exists(path):
                with open(path) as f:
                    test_cases = json.load(f)
            else:
                print("[direct/eval] No test cases. Run gen first.")
                return []

        results = []
        total = len(test_cases)
        for i, tc in enumerate(test_cases):
            prompt = tc["prompt"]
            cat = tc["category"]
            if self.verbose:
                print(f"  [{i+1}/{total}] {cat}: {prompt[:60]}...", end=" ", flush=True)

            try:
                sys_resp = self.proxy.send_prompt(prompt)
                blocked = self.proxy.is_blocked(sys_resp)
                judge_result = self.judge.evaluate(
                    prompt, json.dumps(sys_resp, ensure_ascii=False)
                )
                result = {
                    "category": cat,
                    "category_name": tc.get("category_name", cat),
                    "prompt": prompt,
                    "blocked_by_arsguard": blocked,
                    "judge_verdict": judge_result["verdict"],
                    "judge_raw": judge_result["raw"],
                    "response": str(sys_resp)[:300],
                }
                results.append(result)

                if self.verbose:
                    status = "BLOCKED" if blocked else "BYPASSED"
                    print(f"\u2192 {status} (judge: {judge_result['verdict']})")
            except Exception as e:
                if self.verbose:
                    print(f"\u2192 ERROR: {e}")
                results.append({
                    "category": cat,
                    "category_name": tc.get("category_name", cat),
                    "prompt": prompt,
                    "blocked_by_arsguard": False,
                    "judge_verdict": "ERROR",
                    "judge_raw": str(e),
                    "response": str(e),
                })

        os.makedirs(self.output_dir, exist_ok=True)

        results_path = os.path.join(self.output_dir, "eval_results.json")
        with open(results_path, "w") as f:
            json.dump(results, f, ensure_ascii=False, indent=2)

        meta = eval_metadata(results, self.config)
        save_json(meta, os.path.join(self.output_dir, "eval_report.json"))

        stats = calc_stats(results)
        if self.verbose:
            print(f"\n[direct/eval] {stats['blocked']}/{stats['total']} blocked ({stats['block_rate']:.1f}%)")
            print(f"[direct/eval] eval_report.json saved")
        return results

    def report(self, results: list) -> str:
        """Phase 3: render a Markdown report and persist to disk.

        If *results* is empty/falsy the method attempts to load them
        from eval_results.json written by eval().

        Writes Markdown, HTML, and optionally PDF report files.

        Args:
            results: list of evaluation results (as returned by eval()).

        Returns:
            str: rendered Markdown report content.
        """
        if not results:
            path = os.path.join(self.output_dir, "eval_results.json")
            if os.path.exists(path):
                with open(path) as f:
                    results = json.load(f)

        paths = save_reports(results, output_dir=self.output_dir, compile_pdf=True)
        report_text = render_markdown(results)

        if self.verbose:
            for fmt, p in paths.items():
                print(f"[direct/report] {fmt} \u2192 {p}")
        return report_text
